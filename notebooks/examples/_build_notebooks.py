            "\n",
            "@torch.no_grad()\n",
            "def compute_task_scales(loader, num_tasks, eps=1e-8):\n",
            "    \"\"\"MAD per task from training data, for RAE normalization.\"\"\"\n",
            "    ys = [[] for _ in range(num_tasks)]\n",
            "    for batch in loader:\n",
            "        y = batch.y.view(-1, num_tasks).cpu().numpy()\n",
            "        m = batch.y_mask.view(-1, num_tasks).cpu().numpy().astype(bool)\n",
            "        for t in range(num_tasks):\n",
            "            yt = y[m[:, t], t]\n",
            "            yt = yt[np.isfinite(yt)]\n",
            "            if yt.size:\n",
            "                ys[t].append(yt)\n",
            "    scales = []\n",
            "    for t in range(num_tasks):\n",
            "        if not ys[t]:\n",
            "            scales.append(1.0)\n",
            "            continue\n",
            "        v = np.concatenate(ys[t])\n",
            "        if v.size < 3:\n",
            "            scales.append(1.0)\n",
            "            continue\n",
            "        med = np.median(v)\n",
            "        mad = np.median(np.abs(v - med))\n",
            "        scales.append(float(max(mad, eps)))\n",
            "    return torch.tensor(scales, dtype=torch.float32)\n",
            "\n",
            "\n",
            "def masked_weighted_rae_loss(pred, y, mask, task_scale, eps=1e-8, clip_val=100.0):\n",
            "    \"\"\"Relative absolute error normalized by per-task MAD.\"\"\"\n",
            "    pred = torch.clamp(pred, -clip_val, clip_val)\n",
            "    valid = (mask > 0) & torch.isfinite(y) & torch.isfinite(pred)\n",
            "    diff = torch.where(valid, (pred - y).abs() / (task_scale.to(pred.device) + eps), torch.zeros_like(pred))\n",
            "    w = torch.where(valid, torch.ones_like(pred), torch.zeros_like(pred))\n",
            "    sum_t = (diff * w).sum(dim=0)\n",
            "    sum_w = w.sum(dim=0).clamp_min(eps)\n",
            "    mean_t = sum_t / sum_w\n",
            "    task_mask = w.sum(dim=0) > 0\n",
            "    return mean_t[task_mask].mean() if task_mask.any() else pred.new_tensor(0.0)\n",
            "\n",
            "\n",
            "def masked_weighted_huber_loss(pred, y, mask, delta=1.0, task_scale=None, clip_val=100.0, eps=1e-8):\n",
            "    \"\"\"Huber loss, optionally normalized by task scale.\"\"\"\n",
            "    pred = torch.clamp(pred, -clip_val, clip_val)\n",
            "    valid = (mask > 0) & torch.isfinite(y) & torch.isfinite(pred)\n",
            "    diff = torch.where(valid, pred - y, torch.zeros_like(pred))\n",
            "    if task_scale is not None:\n",
            "        diff = diff / (task_scale.to(diff.device) + eps)\n",
            "    abs_diff = diff.abs()\n",
            "    quad = torch.minimum(abs_diff, abs_diff.new_tensor(delta))\n",
            "    loss = 0.5 * quad ** 2 + delta * (abs_diff - quad)\n",
            "    w = torch.where(valid, torch.ones_like(pred), torch.zeros_like(pred))\n",
            "    sum_t = (loss * w).sum(dim=0)\n",
            "    sum_w = w.sum(dim=0).clamp_min(eps)\n",
            "    mean_t = sum_t / sum_w\n",
            "    task_mask = w.sum(dim=0) > 0\n",
            "    return mean_t[task_mask].mean() if task_mask.any() else pred.new_tensor(0.0)\n",
            "\n",
            "\n",
            "def masked_weighted_corr_loss(pred, y, mask, eps=1e-8, clip_val=100.0):\n",
            "    \"\"\"1 - Pearson correlation per task.\"\"\"\n",
            "    pred = torch.clamp(pred, -clip_val, clip_val)\n",
            "    valid = (mask > 0) & torch.isfinite(y) & torch.isfinite(pred)\n",
            "    w = torch.where(valid, torch.ones_like(pred), torch.zeros_like(pred))\n",
            "    sum_w = w.sum(dim=0).clamp_min(eps)\n",
            "    p_v = torch.where(valid, pred, torch.zeros_like(pred))\n",
            "    y_v = torch.where(valid, y, torch.zeros_like(y))\n",
            "    mean_p = (w * p_v).sum(dim=0) / sum_w\n",
            "    mean_y = (w * y_v).sum(dim=0) / sum_w\n",
            "    p_c = torch.where(valid, p_v - mean_p.unsqueeze(0), torch.zeros_like(pred))\n",
            "    y_c = torch.where(valid, y_v - mean_y.unsqueeze(0), torch.zeros_like(y))\n",
            "    cov = (w * p_c * y_c).sum(dim=0)\n",
            "    std_p = torch.sqrt((w * p_c ** 2).sum(dim=0) + eps)\n",
            "    std_y = torch.sqrt((w * y_c ** 2).sum(dim=0) + eps)\n",
            "    corr = cov / (std_p * std_y + eps)\n",
            "    per_task = 1.0 - corr\n",
            "    task_mask = w.sum(dim=0) > 0\n",
            "    return per_task[task_mask].mean() if task_mask.any() else pred.new_tensor(0.0)\n",
            "\n",
            "\n",
            "def masked_weighted_kendall_rank_loss(pred, y, mask, num_pairs=512, tau_temp=1.0, clip_val=100.0, eps=1e-8):\n",
            "    \"\"\"Differentiable Kendall rank loss via sampled pairs.\"\"\"\n",
            "    pred = torch.clamp(pred, -clip_val, clip_val)\n",
            "    B, T = pred.shape\n",
            "    mask_b = mask.bool()\n",
            "    per_task_loss = []\n",
            "    for t in range(T):\n",
            "        valid_t = mask_b[:, t] & torch.isfinite(y[:, t]) & torch.isfinite(pred[:, t])\n",
            "        idx = torch.where(valid_t)[0]\n",
            "        n = idx.numel()\n",
            "        if n < 2:\n",
            "            per_task_loss.append(pred.new_tensor(0.0))\n",
            "            continue\n",
            "        max_pairs = n * (n - 1) // 2\n",
            "        ii, jj = torch.triu_indices(n, n, offset=1, device=pred.device)\n",
            "        if max_pairs > num_pairs:\n",
            "            probe = min(max_pairs, 8192)\n",
            "            choose = torch.randperm(max_pairs, device=pred.device)[:probe]\n",
            "            ia, ib = ii[choose], jj[choose]\n",
            "            ydiff = (y[idx[ia], t] - y[idx[ib], t]).abs()\n",
            "            topk = torch.topk(ydiff, k=min(num_pairs, probe)).indices\n",
            "            ia, ib = ia[topk], ib[topk]\n",
            "        else:\n",
            "            ia, ib = ii, jj\n",
            "        a, b = idx[ia], idx[ib]\n",
            "        s = torch.sign(y[a, t] - y[b, t])\n",
            "        p_diff = pred[a, t] - pred[b, t]\n",
            "        non_tie = s != 0\n",
            "        if non_tie.any():\n",
            "            per_task_loss.append(F.softplus(-s[non_tie] * p_diff[non_tie] / tau_temp).mean())\n",
            "        else:\n",
            "            per_task_loss.append(pred.new_tensor(0.0))\n",
            "    losses = torch.stack(per_task_loss)\n",
            "    return losses.mean()\n",
            "\n",
            "\n",
            "def masked_r2_style_loss(pred, y, mask, eps=1e-8, clip_val=100.0):\n",
            "    \"\"\"R2-style loss: SSE / Var(y) per task.\"\"\"\n",
            "    pred = torch.clamp(pred, -clip_val, clip_val)\n",
            "    valid = mask.bool() & torch.isfinite(y) & torch.isfinite(pred)\n",
            "    counts = valid.sum(dim=0)\n",
            "    has_data = counts > 1\n",
            "    if not has_data.any():\n",
            "        return pred.new_tensor(0.0)\n",
            "    p_v = torch.where(valid, pred, torch.zeros_like(pred))\n",
            "    y_v = torch.where(valid, y, torch.zeros_like(y))\n",
            "    mean_y = y_v.sum(dim=0) / (counts + eps)\n",
            "    y_c = torch.where(valid, y - mean_y.unsqueeze(0), torch.zeros_like(y))\n",
            "    sse = ((p_v - y_v) ** 2).sum(dim=0)\n",
            "    var_y = (y_c ** 2).sum(dim=0)\n",
            "    good = has_data & (var_y > eps)\n",
            "    if not good.any():\n",
            "        return pred.new_tensor(0.0)\n",
            "    return (sse[good] / (var_y[good] + eps)).mean()\n",
