def make_cell(cell_type, source):
    """Create a Jupyter notebook cell dict."""
    cell = {
        "cell_type": cell_type,
        "metadata": {},
        "source": source,
    }
    if cell_type == "code":
        cell["execution_count"] = None
        cell["outputs"] = []
    return cell


def make_notebook(config_source, shared_cells):
    """Create a full notebook dict."""
    cells = [make_cell("code", config_source)]
    for sc in shared_cells:
        cells.append(make_cell(sc["cell_type"], sc["source"]))
    return {
        "cells": cells,
        "metadata": {
            "kernelspec": {
                "display_name": "Python 3",
                "language": "python",
                "name": "python3",
            },
            "language_info": {
                "name": "python",
                "version": "3.10.0",
            },
        },
        "nbformat": 4,
        "nbformat_minor": 5,
    }


